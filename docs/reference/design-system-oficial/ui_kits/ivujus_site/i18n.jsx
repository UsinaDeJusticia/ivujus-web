// --- Context + hook ---------------------------------------------------
const LanguageContext = React.createContext(null);

function LanguageProvider({ children }) {
  const [lang, setLangState] = React.useState(() => {
    const saved = localStorage.getItem('ivujus-lang');
    if (saved && I18N[saved]) return saved;
    const browser = (navigator.language || 'es').slice(0, 2);
    return I18N[browser] ? browser : 'es';
  });

  React.useEffect(() => {
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('data-lang', lang);
    localStorage.setItem('ivujus-lang', lang);
  }, [lang]);

  const setLang = (l) => { if (I18N[l]) setLangState(l); };
  const t = (key, vars) => {
    let str = (I18N[lang] && I18N[lang][key]) || I18N.es[key] || key;
    if (vars) for (const k in vars) str = str.replace(`{${k}}`, vars[k]);
    return str;
  };
  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

function useT() {
  const ctx = React.useContext(LanguageContext);
  if (!ctx) throw new Error('useT must be used inside <LanguageProvider>');
  return ctx;
}

Object.assign(window, { I18N, LanguageProvider, useT, LanguageContext });
